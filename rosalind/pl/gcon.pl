#!/usr/bin/perl

use strict;
use warnings;
use List::Util 'max';
use constant GAP => -5;

my $score = {};
my @th = split ' ', <DATA>;
while (<DATA>) {
  my @tr = split;
  my $th = shift @tr;
  for my $i (0 .. $#tr) {
    $score->{$th}{$th[$i]} = $tr[$i];
  }
}

chomp(my $s = <>);
chomp(my $t = <>);

my @s = split //, $s;
my @t = split //, $t;

my @dp1 = (0, (GAP) x @t);
my @dp2 = map { $_ + GAP } @dp1;
for my $i (0 .. $#s) {
  for my $j (reverse 0 .. $#t) {
    $dp2[$j + 1] = max($dp1[$j + 1] + GAP, $dp2[$j + 1]);
    $dp1[$j + 1] = max($dp1[$j], $dp2[$j]) + $score->{$s[$i]}{$t[$j]};
  }
  $dp2[0] = max($dp1[0] + GAP, $dp2[0]);
  $dp1[0] = 2 * GAP;
  my $k = 2 * GAP;
  for my $j (0 .. $#t) {
    $k = max($k, $dp1[$j], $dp2[$j]);
    $dp1[$j + 1] = max($dp1[$j + 1], $k + GAP);
  }
}
print max($dp1[-1], $dp2[-1]), "\n";

