int eval(int k, int n) {
  return n / k * 3 + n / 3;
}

int NinjaTurtles::countOpponents(int p, int k) {
  int l = 0, r = k * p;
  while (l < r) {
    int m = (l + r) / 2;
    if (eval(k, m) < p) {
      l = m + 1;
    } else {
      r = m;
    }
  }
  return eval(k, r) == p ? r : -1;
}

