struct CharSwaps {
	int getMinCount(string s);
};

int CharSwaps::getMinCount(string s) {
	int n = s.size(), m = count(s.begin(), s.end(), 'a');
	int ans = n;
	s = s + s;
	FOR(i, n) {
		int tmp = 0;
		FOR(j, m) {
			if (s[i + j] != 'a') {
				++tmp;
			}
		}
		ans = min(ans, tmp);
	}
	return ans;
}

